import fs from "node:fs";
import path from "node:path";

const repoRoot = process.cwd();
const cargoConfigPath = path.join(repoRoot, "src-tauri", ".cargo", "config.toml");

function fail(lines) {
  const message = Array.isArray(lines) ? lines.join("\n") : String(lines);
  console.error(message);
  process.exit(1);
}

function hasBindgenEnvConfig(contents) {
  return contents.includes("[env]") && contents.includes("BINDGEN_EXTRA_CLANG_ARGS");
}

if (process.platform === "win32") {
  if (fs.existsSync(cargoConfigPath)) {
    fail([
      "SubtitleForge preflight failed.",
      "",
      "Detected `src-tauri/.cargo/config.toml` on Windows.",
      "This file is a local platform-specific artifact and can override Windows env-based FFmpeg configuration.",
      "",
      "Fix:",
      "1. Delete `src-tauri/.cargo/config.toml`.",
      "2. Run `scripts\\init-windows.bat` in a fresh terminal.",
      "3. Reopen PowerShell and retry `pnpm tauri dev`.",
    ]);
  }

  const requiredEnvVars = [
    "VCPKG_ROOT",
    "VCPKG_DEFAULT_TRIPLET",
    "VCPKGRS_DYNAMIC",
    "FFMPEG_DIR",
    "BINDGEN_EXTRA_CLANG_ARGS",
    "LIBCLANG_PATH",
  ];
  const missingVars = requiredEnvVars.filter((name) => !process.env[name]);

  if (missingVars.length > 0) {
    fail([
      "SubtitleForge preflight failed.",
      "",
      `Missing Windows build environment variables: ${missingVars.join(", ")}`,
      "Run `scripts\\init-windows.bat`, then reopen your terminal before running `pnpm tauri dev`.",
    ]);
  }

  process.exit(0);
}

const initScript =
  process.platform === "darwin" ? "./scripts/init-macos.sh" : "./scripts/init-linux.sh";

if (!fs.existsSync(cargoConfigPath)) {
  fail([
    "SubtitleForge preflight failed.",
    "",
    `Missing local Cargo config: ${path.relative(repoRoot, cargoConfigPath)}`,
    `Run \`${initScript}\` to generate the platform-specific bindgen config before running Tauri.`,
  ]);
}

const cargoConfig = fs.readFileSync(cargoConfigPath, "utf8");
if (!hasBindgenEnvConfig(cargoConfig)) {
  fail([
    "SubtitleForge preflight failed.",
    "",
    `Invalid local Cargo config: ${path.relative(repoRoot, cargoConfigPath)}`,
    "Expected an `[env]` section with `BINDGEN_EXTRA_CLANG_ARGS` generated by the platform init script.",
    `Re-run \`${initScript}\` to regenerate this file.`,
  ]);
}
